// Variable Declarations

// only string declarations
const constantVariable = "Is not going to change";
console.log(`Constant string: ${constantVariable}`);

let letVariable = "Hello, world!";
console.log(`String variable: ${letVariable}`);

// number declarations
const numberConstant = 32;
console.log(`Declared number constant: ${numberConstant}`);

let number = 100;
console.log(`Declared number variable: ${number}`);

// boolean declarations
const booleanConstant = true;
console.log(`Declared boolean constant: ${booleanConstant}`);

let boolean = false;
console.log(`Declared boolean variable: ${boolean}`);